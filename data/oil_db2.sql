--
-- OILDATA table
--
DROP TABLE OILDATA;
CREATE TABLE OILDATA(
	NAME		VARCHAR(30) NOT NULL,
	capacity	INT,
	PRICE		DECIMAL(6,2),
	OCTANE		DECIMAL(6,2),
	LEAD		DECIMAL(6,2),
	PRIMARY KEY (NAME)
);
